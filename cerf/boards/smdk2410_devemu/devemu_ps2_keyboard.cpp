uint32_t DevEmuPs2Keyboard::ReadWord(uint32_t addr) {
    const uint32_t off = addr - MmioBase();
    uint32_t value = 0;
    bool     keys_remain_after_pop = false;
    {
        std::lock_guard<std::mutex> lk(state_mutex_);
        switch (off) {
            case kRegSpcon:
                value = spcon_;
                break;
            case kRegSpsta:
                value = spsta_;
                break;
            case kRegSppin:
                value = sppin_;
                break;
            case kRegSppre:
                value = sppre_;
                break;
            case kRegSptdat:
                value = sptdat_;
                break;
            case kRegSprdat:
                if (sptdat_ == 0xFF && queue_head_ != queue_tail_) {
                    sprdat_     = queue_[queue_tail_];
                    queue_tail_ = (queue_tail_ + 1) % kQueueLen;
                    keys_remain_after_pop = (queue_head_ != queue_tail_);
                } else {
                    sprdat_ = 0;
                }
                value = sprdat_;
                break;
            default:
                HaltUnsupportedAccess("ReadWord", addr, 0);  /* noreturn */
        }
    }
    /* Re-raise outside state_mutex_ — IrqController has its own lock
       and we don't want to hold ours across the call. */
    if (keys_remain_after_pop) {
        emu_.Get<IrqController>().AssertIrq(kIrqEint1);
    }
    return value;
}

void DevEmuPs2Keyboard::WriteWord(uint32_t addr, uint32_t value) {
    const uint32_t off = addr - MmioBase();
    LOG(Periph, "[Ps2Kbd] write +0x%02X = 0x%08X\n", off, value);
    std::lock_guard<std::mutex> lk(state_mutex_);
    switch (off) {
        case kRegSpcon:
            spcon_ = value;
            break;
        case kRegSpsta:
            /* Read-only on the real SPI controller; drop the write. */
            break;
        case kRegSppin:
            sppin_ = value;
            break;
        case kRegSppre:
            sppre_ = value;
            break;
        case kRegSptdat:
            sptdat_ = static_cast<uint8_t>(value);
            spsta_ |= kSpstaRedy;
            break;
        case kRegSprdat:
            sprdat_ = static_cast<uint8_t>(value);
            break;
        default:
            HaltUnsupportedAccess("WriteWord", addr, value);  /* noreturn */
    }
}

void DevEmuPs2Keyboard::EnqueueScancode(uint8_t sc) {
    bool raise = false;
    {
        std::lock_guard<std::mutex> lk(state_mutex_);
        const uint32_t smod = (spcon_ & kSpconSmodMask) >> kSpconSmodShift;
        if (smod != kSpconSmodIntMode) {
            /* Keyboard disabled by guest driver — drop the key on the
               floor, matching the BSP's IOSPI1::EnqueueKey behavior. */
            return;
        }
        queue_[queue_head_] = sc;
        queue_head_ = (queue_head_ + 1) % kQueueLen;
        /* No overflow check — head wrapping into tail silently
           overwrites the oldest pending scancode, also matching the
           BSP's behavior. */
        raise = true;
    }
    if (raise) {
        LOG(Periph, "[Ps2Kbd] enqueue 0x%02X, raising EINT1\n", sc);
        emu_.Get<IrqController>().AssertIrq(kIrqEint1);
    }
}

void DevEmuPs2Keyboard::OnHostKey(uint8_t vk, bool key_up) {
    /* Win32 reports VK_SHIFT (0x10) for either L or R shift; the
       BSP table only has scancodes at the VK_LSHIFT slot (0xA0), so
       remap before lookup. */
    if (vk == kVkShift) vk = kVkLShift;

    const uint8_t key_state = key_up ? kScKeyUp : kScKeyDown;

    /* Direct mapping — vast majority of keys land here. */
    if (kVkToScTable[vk] != 0) {
        EnqueueScancode((uint8_t)(kVkToScTable[vk] | key_state));
        return;
    }

    /* No direct match — try function-key map first, then numlock map. */
    uint8_t mapped_vk        = 0;
    bool    from_numlock_map = false;
    for (const auto& m : kVkMapFn) {
        if (m.host_vk == vk) { mapped_vk = m.guest_vk; break; }
    }
    if (mapped_vk == 0) {
        for (const auto& m : kVkMapNumLock) {
            if (m.host_vk == vk) {
                mapped_vk        = m.guest_vk;
                from_numlock_map = true;
                break;
            }
        }
        /* If we matched via the numlock table and the guest currently
           has NumLock on, flip it off first with the "Fn + - down + -
           up + Fn" sequence. Only fires once because we update
           num_lock_state_ below. */
        if (from_numlock_map && mapped_vk != 0 && num_lock_state_) {
            EnqueueScancode((uint8_t)(kVkToScTable[kVkMatrixFn] | kScKeyDown));
            EnqueueScancode((uint8_t)(kVkToScTable[kVkHyphen]   | kScKeyDown));
            EnqueueScancode((uint8_t)(kVkToScTable[kVkHyphen]   | kScKeyUp));
            EnqueueScancode((uint8_t)(kVkToScTable[kVkMatrixFn] | kScKeyUp));
            num_lock_state_ = false;
        }
    }

    if (mapped_vk == 0) {
        /* No mapping found anywhere; drop silently (matches BSP). */
        return;
    }

    /* Toggle our shadow numlock state on a numlock-key down event
       (mirrors the BSP's "if (pvkMapMatch == VK_HYPHEN && KeyUpDown
       == 0) m_NumLockState = !m_NumLockState"). */
    if (mapped_vk == kVkHyphen && !key_up) {
        num_lock_state_ = !num_lock_state_;
    }

    /* Send the wrapped sequence: Fn-down + mapped key + Fn-up. */
    EnqueueScancode((uint8_t)(kVkToScTable[kVkMatrixFn] | kScKeyDown));
    EnqueueScancode((uint8_t)(kVkToScTable[mapped_vk]   | key_state));
    EnqueueScancode((uint8_t)(kVkToScTable[kVkMatrixFn] | kScKeyUp));
}
